<script>
document.addEventListener("DOMContentLoaded", function() {
  // All blog posts
  const blogPosts = document.querySelectorAll(".blog-post");
  const postsPerPage = [4, 4, 2];  // Posts per page for each page
  const totalPages = postsPerPage.length; // Number of pages based on the postsPerPage array

  // Function to show a specific page
  function showPage(pageNumber) {
    // Calculate the starting and ending post indices for the selected page
    let start = 0;
    let end = 0;
    
    // Calculate the start and end for each page based on the postsPerPage array
    for (let i = 0; i < pageNumber - 1; i++) {
      start += postsPerPage[i]; // Start index
    }
    end = start + postsPerPage[pageNumber - 1]; // End index

    // Hide all posts
    blogPosts.forEach((post, index) => {
      post.style.display = "none";
    });

    // Show only the posts for the current page
    for (let i = start; i < end && i < blogPosts.length; i++) {
      blogPosts[i].style.display = "block";
    }

    // Update the pagination link styling
    document.querySelectorAll(".pagination .page-link").forEach((link, index) => {
      link.classList.remove("active");
      if (index + 1 === pageNumber) {
        link.classList.add("active");
      }
    });
  }

  // Initialize the first page
  showPage(1);

  // Attach the function to window so it works in inline onclick
  window.showPage = showPage;
});

</script>
