                    const dateInput = document.getElementById(inputId).value;
                    if (dateInput) {  
                        const dateObj = new Date(dateInput);
                        const options = { month: 'short', day: 'numeric' };
                        const formattedDate = dateObj.toLocaleDateString('en-US', options);
